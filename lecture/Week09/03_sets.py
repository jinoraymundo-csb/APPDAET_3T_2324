# lists
l = [1, 2, 3, 1]

# dictionaries
d = {"1": 1, "2": 2, "3": 3}

# sets
s = {1, 2, 3, 1}

print(f"List: {l}")
print(f"Dictionary: {d}")
print(f"Set: {s}")

l2 = list([1, 2, 3, 1])
d2 = dict(first=1, second=2, third=3)
s2 = set((1, 2, 3, 1))

print(f"List: {l2}")
print(f"Dictionary: {d2}")
print(f"Set: {s2}")